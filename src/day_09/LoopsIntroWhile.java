package day_09;

public class LoopsIntroWhile {
	
	public static void main(String[] args) {
		
		
		//LOOPS - Iteration
		
		//print the numbers from 1 to 5
		
		int num = 1;
		
		while (num <= 5) {
			
			System.out.println(num);
			
			num ++;
			
		}
		
		System.out.println("Counting finished!");
		
	}

}
